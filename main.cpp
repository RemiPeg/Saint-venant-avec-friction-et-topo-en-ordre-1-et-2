#include "DataFile.h"
#include "Mesh.h"
#include "Physics.h"
#include "FiniteVolume.h"
#include "TimeScheme.h"

#include <iostream>



int main(int argc, char** argv)
{
  //-------------------------------------------------------//
  //---------------------Vérifications---------------------//
  //-------------------------------------------------------//
  if (argc < 2)
    {
      std::cout <<  "Please, enter the name of your data file." << std::endl;
      exit(-1);
    }


  //-------------------------------------------------------//
  //---------------------Logs de début---------------------//
  //-------------------------------------------------------//
  std::cout << std::endl << "====================================================================================================" << std::endl;
  std::cout << "Solving 1D St-Venant equations for you !" << std::endl;
  std::cout << "====================================================================================================" << std::endl << std::endl;


  //-------------------------------------------------------//
  //---------------------Fichier de paramètres-------------//
  //-------------------------------------------------------//
  DataFile* DF = new DataFile(argv[1]);
  DF->readDataFile();
  std::cout << std::endl << "====================================================================================================" << std::endl;
  std::cout << "Reading file !" << std::endl;
  std::cout << "====================================================================================================" << std::endl << std::endl;


  //--------------------------------------------------//
  //---------------------Maillage---------------------//
  //--------------------------------------------------//
  Mesh* mesh = new Mesh(DF);
  mesh->Initialize();
  std::cout << std::endl << "====================================================================================================" << std::endl;
  std::cout << "Meshing !" << std::endl;
  std::cout << "====================================================================================================" << std::endl << std::endl;


  //----------------------------------------------------------------//
  //---------------------CI, CL, Termes sources---------------------//
  //----------------------------------------------------------------//
  Physics* physics = new Physics(DF, mesh);
  physics->Initialize();
  std::cout << std::endl << "====================================================================================================" << std::endl;
  std::cout << "Physic calculation !" << std::endl;
  std::cout << "====================================================================================================" << std::endl << std::endl;


  //--------------------------------------------------------//
  //---------------------Flux numérique---------------------//
  //--------------------------------------------------------//
  FiniteVolume* finVol;
  finVol ->Initialize(DF, mesh, physics);
  std::cout << std::endl << "====================================================================================================" << std::endl;
  std::cout << "Finite Volume methode !" << std::endl;
  std::cout << "====================================================================================================" << std::endl << std::endl;


  //---------------------------------------------------------//
  //---------------------Schéma en temps---------------------//
  //---------------------------------------------------------//
  TimeScheme* TS;
  if (DF->isfriction_yes()==0)
    {
      TS = new SV(DF, mesh, physics, finVol);
    }
  else 
    {
      TS = new SV_f(DF, mesh, physics, finVol);
    }

      std::cout << std::endl << "====================================================================================================" << std::endl;
      std::cout << "Time !" << std::endl;
      std::cout << "====================================================================================================" << std::endl << std::endl;

  //----------------------------------------------------//
  //---------------------Résolution---------------------//
  //----------------------------------------------------//
  TS->Solve();
  std::cout << std::endl << "====================================================================================================" << std::endl;
  std::cout << "Solve !" << std::endl;
  std::cout << "====================================================================================================" << std::endl << std::endl;


  //-----------------------------------------------------------//
  //---------------------Libère la mémoire---------------------//
  //-----------------------------------------------------------//
  delete DF;
  delete mesh;
  delete physics;
  delete finVol;
  delete TS;


  //-----------------------------------------------------//
  //---------------------Logs de fin---------------------//
  //-----------------------------------------------------//
  std::cout << std::endl << "====================================================================================================" << std::endl;
  std::cout <<  "SUCCESS : Successfully solved the 1D St-Venant equations for you !" << std::endl;
  std::cout <<  "Let me terminate myself now..." << std::endl;
  std::cout << "====================================================================================================" << std::endl << std::endl;


  // Fin
  return EXIT_SUCCESS;
}
